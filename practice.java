class Arith {
    public static void main(String[] args) {

        int add = 5 + 2;
        System.out.println("Add:" + add);

        int sub = 5 - 2;
        System.out.println("sub:" + sub);

        int mul = 5 * 2;
        System.out.println("mul:" + mul);

        int div = 5 + 2;
        System.out.println("div:" + div);

        int mod = 5 % 2;
        System.out.println("Add:" + mod);

    }
}